    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        Double notaa, notab, notac;
        Double prom;
        String situacion;
        
        // Recibiendo Datos
        notaa = Double.parseDouble(txtnotaa.getText());
        notab = Double.parseDouble(txtnotab.getText());
        notac = Double.parseDouble(txtnotac.getText());
        
        // Proceso
        prom = ((notaa*3)+(notab*4)+(notac*2))/9;
        
        // Evaluando al alumno
        if (prom >= 10.5) {
            situacion = "Aprobado";
        }
        else {
            situacion = "Desaprobado";
        }
        
        // Salida de información
        txtpromedio.setText(Double.toString(prom));
        JOptionPane.showMessageDialog(null,"Usted está " + situacion,"Promedio",JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btncalcularActionPerformed

