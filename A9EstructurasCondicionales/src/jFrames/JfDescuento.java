    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        
        // Declarando Variables
        Integer cantidad;
        double punitario, pparcial, desc, pneto;
        desc = 0.0;
        
        // Recibiendo datos
        cantidad = Integer.parseInt(txtcantidad.getText());
        punitario = Double.parseDouble(txtpreciounitario.getText());
        
        // Proceso
        pparcial = cantidad * punitario;
        
        // CondiciÃ³n para establecer el descuento
        if (pparcial > 150) {
            desc = pparcial * 7/100;
        }
        
        pneto = pparcial-desc;
        
        // Salida
        txtprecioparcial.setText(Double.toString(pparcial));
        txtdescuento.setText(Double.toString(desc));
        txtprecioneto.setText(Double.toString(pneto));
        
    }//GEN-LAST:event_btncalcularActionPerformed

