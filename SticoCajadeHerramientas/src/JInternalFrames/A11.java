    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // Declarando variables
        Integer numalumnos;
        Double nota,sumanotas,notabaja,promedio;

        // Entrada de datos e inicializacion
        notabaja = 20.0;
        sumanotas = 0.0;
        numalumnos = Integer.parseInt(txtnumero.getText());

        // Proceso
        // Validar que se ingresó algo
        // Bucle dependiendo de la cantida de alumnos, por cada uno pedimos una nota

        if (!txtnumero.getText().equals("")) {
            for (int i =1;i<=numalumnos; i= i+1) {
                nota = Double.parseDouble(JOptionPane.showInputDialog("Ingrese la nota del alumno" + i));
                sumanotas  = sumanotas + nota;
                if (nota<notabaja) {
                    notabaja = nota;
                }
            }
            promedio = sumanotas / numalumnos;

            // Salida
            txtpromedio.setText(Double.toString(promedio));
            txtnotabaja.setText(Double.toString(notabaja));
        }

    }//GEN-LAST:event_btncalcularActionPerformed

