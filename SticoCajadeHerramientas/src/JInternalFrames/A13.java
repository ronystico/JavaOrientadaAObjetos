    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // Declarando Variables
        byte cantAlumnos,nota;
        int suma = 0;
        double promedio;

        // Entrada de datos
        cantAlumnos = Byte.parseByte(txtCantAlumnos.getText());

        // Proceso
        // Un bucle
        for (int i =1;i<=cantAlumnos;i=i+1) {
            do {
                nota = Byte.parseByte(JOptionPane.showInputDialog(this,
                    "Ingrese la nota del alumno "+ i,"Notas",1));
        } while (nota<0 || nota>20);

        suma = suma+nota;
        }
        promedio = suma/cantAlumnos;

        // Salida
        txtPromedio.setText(Double.toString(promedio));

    }//GEN-LAST:event_btningresarActionPerformed

