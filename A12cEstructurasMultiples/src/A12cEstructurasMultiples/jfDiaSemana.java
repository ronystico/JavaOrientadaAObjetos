    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // Declarar variables
        Integer numero;
        String dia;
        dia = "";
        
        // Entrada de los datos
        numero = Integer.parseInt(txtnumero.getText());
        
        // Proceso
        if (!txtnumero.getText().equals("")) {
            // Estructura selectiva en este caso múltiple
            switch (numero) {
                case 1:
                    dia = "Lunes";
                    break;
                case 2:
                    dia = "Martes";
                    break;
                case 3:
                    dia = "Miércoles";
                    break;
                case 4:
                    dia = "Jueves";
                    break;
                case 5:
                    dia = "Viernes";
                    break;
                case 6:
                    dia = "Sábado";
                    break;
                case 7:
                    dia = "Domingo";
                    break;
                default:
                    JOptionPane.showMessageDialog(rootPane, "Ingrese un valor entre 1 y 7","Día de la semana",JOptionPane.ERROR_MESSAGE);  
            }
        }
            else {
                    JOptionPane.showMessageDialog(rootPane, "Ingrese un valor numérico válido");
                    }

        // Salida de la información
        txtdia.setText(dia);
        
    }//GEN-LAST:event_btncalcularActionPerformed

